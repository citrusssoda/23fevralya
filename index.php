<div class="block">
	<p>День защитника Отечества. 23 февраля в России отмечается День защитника Отечества, являющейся днем воинской славы страны. Праздник зародился еще в СССР; тогда 23 февраля ежегодно отмечался как всенародный праздник – День Советской Армии и Военно-Морского Флота.
</p><br>
<center> <img src="https://ds04.infourok.ru/uploads/ex/1047/00151ee6-f6ccb512/hello_html_m8882c2d.jpg" width="750"><br><br><img src="https://www.culture.ru/storage/images/17fd9ba740c5adcf6a5ba8aacdc50c3f/0dad76435dbb3ae630a8c52e7ce130fb.jpeg" width="750"></center>
<br>
<p>Праздник, отмечаемый ежегодно 23 февраля в Белоруссии, Киргизии, России и Таджикистане. Был установлен в РСФСР 27 января 1922 года, когда Президиум ВЦИК РСФСР опубликовал постановление о четвёртой годовщине Красной армии, в котором говорилось: «В соответствии с постановлением IX Всероссийского съезда Советов о Красной армии Президиум ВЦИК обращает внимание исполкомов на наступающую годовщину создания Красной армии».
</p><br>
